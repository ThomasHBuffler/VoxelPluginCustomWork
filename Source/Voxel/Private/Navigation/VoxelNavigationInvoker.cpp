TSharedRef<FVoxelNavigationInvokerView> FVoxelNavigationInvokerManager::MakeView(
	const int32 ChunkSize,
	const FTransform& LocalToWorld)
{
	VOXEL_FUNCTION_COUNTER();
	check(IsInGameThread());

	const TSharedRef<FVoxelNavigationInvokerView> View = MakeShared<FVoxelNavigationInvokerView>(
		ChunkSize,
		LocalToWorld);

	// Ensure the view is up to date in the first query
	if (const UNavigationSystemV1* NavSys = FNavigationSystem::GetCurrent<UNavigationSystemV1>(GetWorld().Resolve()))
	{
		FVoxelTaskScope Scope(*GVoxelGlobalTaskContext);
		View->Tick(NavSys->GetInvokerLocations());
	}
	else
	{
		FVoxelTaskScope Scope(*GVoxelGlobalTaskContext);
		View->Tick({});
	}

	WeakViews.Add(View);
	return View;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

void FVoxelNavigationInvokerManager::Tick()
{
	VOXEL_FUNCTION_COUNTER();
	check(IsInGameThread());

	const double Time = FPlatformTime::Seconds();
	if (Time < LastTickTime + 0.05)
	{
		return;
	}
	LastTickTime = Time;

	const UNavigationSystemV1* NavSys = FNavigationSystem::GetCurrent<UNavigationSystemV1>(GetWorld().Resolve());
	for (auto It = WeakViews.CreateIterator(); It; ++It)
	{
		const TSharedPtr<FVoxelNavigationInvokerView> View = It->Pin();
		if (!View)
		{
			It.RemoveCurrent();
			continue;
		}

		if (NavSys)
		{
			View->Tick(NavSys->GetInvokerLocations());
		}
		else
		{
			View->Tick({});
		}
	}
}