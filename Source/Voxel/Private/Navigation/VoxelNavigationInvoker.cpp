FVoxelNavigationInvokerView::FVoxelNavigationInvokerView(
	const int32 ChunkSize,
	const FTransform& LocalToWorld)
	: ChunkSize(ChunkSize)
	, LocalToWorld(LocalToWorld)
	, Dependency(FVoxelDependency::Create("FVoxelNavigationInvokerView"))
{
	ensure(ChunkSize > 0);
}

TVoxelFuture<const TVoxelSet<FIntVector>> FVoxelNavigationInvokerView::GetChunks(FVoxelDependencyCollector& DependencyCollector) const
{
	DependencyCollector.AddDependency(*Dependency);

	VOXEL_SCOPE_LOCK(CriticalSection);

	if (!Chunks_RequiresLock)
	{
		// Async to avoid recursive locks
		return Future.Then_AsyncThread(MakeStrongPtrLambda(this, [this]
		{
			VOXEL_SCOPE_LOCK(CriticalSection);
			return Chunks_RequiresLock.ToSharedRef();
		}));
	}

	return Chunks_RequiresLock.ToSharedRef();
}

void FVoxelNavigationInvokerView::Tick(const TArray<FNavigationInvokerRaw>& InvokerLocations)
{
	VOXEL_FUNCTION_COUNTER();

	if (!Future.IsComplete())
	{
		return;
	}

	VOXEL_SCOPE_COUNTER_FORMAT("InvokerLocations.Num() = %d", InvokerLocations.Num());

	TVoxelArray<FSphere> Invokers;
	Invokers.Reserve(InvokerLocations.Num());

	for (const FNavigationInvokerRaw& Invoker : InvokerLocations)
	{
		Invokers.Add(FSphere
		{
			Invoker.Location,
			Invoker.RadiusMax
		});
	}

	if (Invokers == LastInvokers)
	{
		VOXEL_SCOPE_LOCK(CriticalSection);
		if (!Chunks_RequiresLock)
		{
			Chunks_RequiresLock = MakeShared<TVoxelSet<FIntVector>>();
		}
		return;
	}

	LastInvokers = Invokers;

	ensure(Future.IsComplete());

	Future = Voxel::AsyncTask(MakeWeakPtrLambda(this, [this, Invokers = MakeSharedCopy(MoveTemp(Invokers))]
	{
		VOXEL_SCOPE_COUNTER("FVoxelNavigationInvokerView");

		const TSharedRef<TVoxelSet<FIntVector>> NewChunks = MakeShared<TVoxelSet<FIntVector>>();

		if (!FVoxelUtilities::ComputeInvokerChunks(
			*NewChunks,
			MoveTemp(*Invokers),
			LocalToWorld,
			ChunkSize,
			1024 * 1024))
		{
			VOXEL_MESSAGE(Error, "More than 1M chunks generated by Navigation invoker for chunk size {1}, abording", ChunkSize);
		}

		{
			VOXEL_SCOPE_LOCK(CriticalSection);

			if (!Chunks_RequiresLock)
			{
				// Initialization, don't invalidate dependency
				Chunks_RequiresLock = NewChunks;
				return;
			}

			if (NewChunks->OrderIndependentEqual(*Chunks_RequiresLock))
			{
				return;
			}

			Chunks_RequiresLock = NewChunks;
		}

		FVoxelInvalidationScope Scope("FVoxelNavigationInvokerView::Tick");
		Dependency->Invalidate();
	}));
}

