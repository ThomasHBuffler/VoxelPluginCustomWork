FVoxelCollisionInvokerView::FVoxelCollisionInvokerView(
	const int32 ChunkSize,
	const FTransform& LocalToWorld)
	: ChunkSize(ChunkSize)
	, LocalToWorld(LocalToWorld)
	, Dependency(FVoxelDependency::Create("FVoxelCollisionInvokerView"))
{
	ensure(ChunkSize > 0);
}

TVoxelFuture<const TVoxelSet<FIntVector>> FVoxelCollisionInvokerView::GetChunks(FVoxelDependencyCollector& DependencyCollector) const
{
	DependencyCollector.AddDependency(*Dependency);

	VOXEL_SCOPE_LOCK(CriticalSection);

	if (!Chunks_RequiresLock)
	{
		// Async to avoid recursive locks
		return Future.Then_AsyncThread(MakeStrongPtrLambda(this, [this]
		{
			VOXEL_SCOPE_LOCK(CriticalSection);
			return Chunks_RequiresLock.ToSharedRef();
		}));
	}

	return Chunks_RequiresLock.ToSharedRef();
}

void FVoxelCollisionInvokerView::Tick(const TVoxelSet<UVoxelCollisionInvokerComponent*>& InvokerComponents)
{
	VOXEL_FUNCTION_COUNTER();

	if (!Future.IsComplete())
	{
		return;
	}

	VOXEL_SCOPE_COUNTER_FORMAT("InvokerComponents.Num() = %d", InvokerComponents.Num());

	TVoxelArray<FSphere> Invokers;
	Invokers.Reserve(InvokerComponents.Num());

#if !UE_BUILD_SHIPPING
	TVoxelArray<TVoxelObjectPtr<const UVoxelCollisionInvokerComponent>> NewInvokerComponents;
	NewInvokerComponents.Reserve(InvokerComponents.Num());
#endif

	for (const UVoxelCollisionInvokerComponent* InvokerComponent : InvokerComponents)
	{
		if (!ensure(InvokerComponent) ||
			!InvokerComponent->bEnabled)
		{
			continue;
		}

#if !UE_BUILD_SHIPPING
		NewInvokerComponents.Add(InvokerComponent);
#endif

		Invokers.Add(FSphere
		{
			InvokerComponent->GetComponentLocation(),
			InvokerComponent->Radius
		});
	}

	if (Invokers == LastInvokers)
	{
		VOXEL_SCOPE_LOCK(CriticalSection);
		if (!Chunks_RequiresLock)
		{
			Chunks_RequiresLock = MakeShared<TVoxelSet<FIntVector>>();
		}
		return;
	}

	FString ComponentChanges = "FVoxelCollisionInvokerView changed:";
#if VOXEL_INVALIDATION_TRACKING
	if (NewInvokerComponents == LastInvokerComponents)
	{
		check(Invokers.Num() == LastInvokers.Num());

		for (int32 Index = 0; Index < LastInvokers.Num(); Index++)
		{
			if (LastInvokers[Index] == Invokers[Index])
			{
				continue;
			}

			const UVoxelCollisionInvokerComponent* InvokerComponent = NewInvokerComponents[Index].Resolve();
			if (!ensureVoxelSlow(InvokerComponent))
			{
				continue;
			}

			ComponentChanges += "\n\t" + InvokerComponent->GetPathName() + ": " + LexToString(LastInvokers[Index]) + " -> " + LexToString(Invokers[Index]);
		}
	}
	else
	{
		const TVoxelSet<TVoxelObjectPtr<const UVoxelCollisionInvokerComponent>> NewInvokerComponentsSet(NewInvokerComponents);
		const TVoxelSet<TVoxelObjectPtr<const UVoxelCollisionInvokerComponent>> LastInvokerComponentsSet(LastInvokerComponents);

		for (const TVoxelObjectPtr<const UVoxelCollisionInvokerComponent> InvokerComponent : NewInvokerComponentsSet.Difference(LastInvokerComponentsSet))
		{
			ComponentChanges += "\n\t" + InvokerComponent.GetPathName() + " added";
		}
		for (const TVoxelObjectPtr<const UVoxelCollisionInvokerComponent> InvokerComponent : LastInvokerComponentsSet.Difference(NewInvokerComponentsSet))
		{
			ComponentChanges += "\n\t" + InvokerComponent.GetPathName() + " removed";
		}
	}

	LastInvokerComponents = NewInvokerComponents;
#endif

	LastInvokers = Invokers;

	ensure(Future.IsComplete());

	Future = Voxel::AsyncTask(MakeWeakPtrLambda(this, [this, Invokers = MakeSharedCopy(MoveTemp(Invokers)), ComponentChanges = MoveTemp(ComponentChanges)]
	{
		VOXEL_SCOPE_COUNTER("FVoxelCollisionInvokerView");

		const TSharedRef<TVoxelSet<FIntVector>> NewChunks = MakeShared<TVoxelSet<FIntVector>>();

		if (!FVoxelUtilities::ComputeInvokerChunks(
			*NewChunks,
			MoveTemp(*Invokers),
			LocalToWorld,
			ChunkSize,
			1024 * 1024))
		{
			VOXEL_MESSAGE(Error, "More than 1M chunks generated by collision invoker for chunk size {0}, abording", ChunkSize);
		}

		{
			VOXEL_SCOPE_LOCK(CriticalSection);

			if (!Chunks_RequiresLock)
			{
				// Initialization, don't invalidate dependency
				Chunks_RequiresLock = NewChunks;
				return;
			}

			if (NewChunks->OrderIndependentEqual(*Chunks_RequiresLock))
			{
				return;
			}

			Chunks_RequiresLock = NewChunks;
		}

		FVoxelInvalidationScope Scope(ComponentChanges);
		Dependency->Invalidate();
	}));
}

