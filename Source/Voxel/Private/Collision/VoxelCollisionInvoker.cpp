void FVoxelCollisionInvokerManager::LogInvokers()
{
	VOXEL_FUNCTION_COUNTER();
	checkVoxelSlow(IsInGameThread());

	const UWorld* World = GetWorld().Resolve();
	if (!ensure(World))
	{
		return;
	}

	LOG_VOXEL(Log, "Collision invokers for %s:", *World->GetPathName());

	for (const UVoxelCollisionInvokerComponent* Invoker : InvokerComponents)
	{
		LOG_VOXEL(Log, "\tEnabled: %s Radius: %f %s", *LexToString(Invoker->bEnabled), Invoker->Radius, *Invoker->GetPathName());
	}
}

TSharedRef<FVoxelCollisionInvokerView> FVoxelCollisionInvokerManager::MakeView(
	const int32 ChunkSize,
	const FTransform& LocalToWorld)
{
	VOXEL_FUNCTION_COUNTER();
	check(IsInGameThread());

	const TSharedRef<FVoxelCollisionInvokerView> View = MakeShared<FVoxelCollisionInvokerView>(
		ChunkSize,
		LocalToWorld);

	// Ensure the view is up to date in the first query
	{
		FVoxelTaskScope Scope(*GVoxelGlobalTaskContext);
		View->Tick(InvokerComponents);
	}

	WeakViews.Add(View);
	return View;
}

