void FVoxelCollisionInvokerManager::Tick()
{
	VOXEL_FUNCTION_COUNTER();

	CheckVoxelWorlds();

	for (auto It = WeakViews.CreateIterator(); It; ++It)
	{
		const TSharedPtr<FVoxelCollisionInvokerView> View = It->Pin();
		if (!View)
		{
			It.RemoveCurrentSwap();
			continue;
		}

		View->Tick(InvokerComponents);
	}
}

void FVoxelCollisionInvokerManager::AddReferencedObjects(FReferenceCollector& Collector)
{
	VOXEL_FUNCTION_COUNTER();

	for (auto It = InvokerComponents.CreateIterator(); It; ++It)
	{
		UVoxelCollisionInvokerComponent* InvokerComponentPtr = *It;
		TObjectPtr<UVoxelCollisionInvokerComponent> InvokerComponent = ReinterpretCastRef<TObjectPtr<UVoxelCollisionInvokerComponent>>(InvokerComponentPtr);
		Collector.AddReferencedObject(InvokerComponent);

		if (!ensure(InvokerComponent))
		{
			It.RemoveCurrent();
		}
	}
}

void FVoxelCollisionInvokerManager::CheckVoxelWorlds()
{
	VOXEL_FUNCTION_COUNTER();

	TVoxelArray<FVector> Positions;
	Positions.Reserve(InvokerComponents.Num());

	for (const UVoxelCollisionInvokerComponent* InvokerComponent : InvokerComponents)
	{
		if (!InvokerComponent->bEnabled ||
			!InvokerComponent->bWaitForVoxelWorld)
		{
			continue;
		}

		Positions.Add(InvokerComponent->GetComponentLocation());
	}

	if (Positions.Num() == 0)
	{
		return;
	}

	UWorld* World = GetWorld().Resolve();
	if (!ensure(World))
	{
		return;
	}

	for (const AVoxelWorld* VoxelWorld : TActorRange<AVoxelWorld>(World))
	{
		const TSharedPtr<FVoxelRuntime> Runtime = VoxelWorld->GetRuntime();
		if (!Runtime)
		{
			continue;
		}

		const TSharedPtr<FVoxelState> State = Runtime->GetNewestState();
		if (!State ||
			State->Config->InvokerCollision.GetCollisionEnabled() == ECollisionEnabled::NoCollision)
		{
			continue;
		}

		FVoxelInvokerCollisionSubsystem& Subsystem = State->GetSubsystem<FVoxelInvokerCollisionSubsystem>();
		if (!Subsystem.HasCollision(Positions))
		{
			Subsystem.ComputeInline(*Runtime, Positions);
		}
	}
}

