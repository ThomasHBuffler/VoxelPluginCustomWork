const UVoxelTerminalGraph* UVoxelGraphNode_CallExternalFunction::GetBaseTerminalGraph() const
{
	if (!FunctionLibrary)
	{
		return nullptr;
	}

	return FunctionLibrary->GetGraph().FindTerminalGraph(Guid);
}

void UVoxelGraphNode_CallExternalFunction::AllocateDefaultPins()
{
	if (const UVoxelTerminalGraph* BaseTerminalGraph = GetBaseTerminalGraph())
	{
		CachedName = BaseTerminalGraph->GetDisplayName();

		OnFunctionMetaDataChangedPtr = MakeSharedVoid();
		GVoxelGraphTracker->OnTerminalGraphMetaDataChanged(*BaseTerminalGraph).Add(FOnVoxelGraphChanged::Make(OnFunctionMetaDataChangedPtr, this, [=, this]
		{
			RefreshNode();
		}));
	}

	Super::AllocateDefaultPins();
}

FText UVoxelGraphNode_CallExternalFunction::GetNodeTitle(const ENodeTitleType::Type TitleType) const
{
	if (GetBaseTerminalGraph())
	{
		return Super::GetNodeTitle(TitleType);
	}

	return FText::FromString(CachedName);
}

bool UVoxelGraphNode_CallExternalFunction::CanPasteHere(const UEdGraph* TargetGraph) const
{
	if (!Super::CanPasteHere(TargetGraph))
	{
		return false;
	}

	if (!ensure(FunctionLibrary))
	{
		return false;
	}

	const UVoxelEdGraph* VoxelGraph = Cast<UVoxelEdGraph>(TargetGraph);
	if (!VoxelGraph)
	{
		return false;
	}

	const TSharedPtr<FVoxelGraphToolkit> Toolkit = VoxelGraph->GetGraphToolkit();
	if (!Toolkit ||
		!Toolkit->Asset)
	{
		return false;
	}

	return FunctionLibrary->GetGraph().FindTerminalGraph(Guid)->CanBePlaced(*Toolkit->Asset);
}