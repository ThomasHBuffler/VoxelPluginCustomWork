const UVoxelTerminalGraph* UVoxelGraphNode_CallMemberFunction::GetBaseTerminalGraph() const
{
	const UVoxelGraph* Graph = GetTypedOuter<UVoxelGraph>();
	if (!ensure(Graph))
	{
		return nullptr;
	}

	if (!bCallParent)
	{
		return Graph->FindTerminalGraph(Guid);
	}

	const UVoxelGraph* BaseGraph = Graph->GetBaseGraph_Unsafe();
	if (!BaseGraph)
	{
		return nullptr;
	}

	return BaseGraph->FindTerminalGraph(Guid);
}

void UVoxelGraphNode_CallMemberFunction::AllocateDefaultPins()
{
	if (const UVoxelTerminalGraph* BaseTerminalGraph = GetBaseTerminalGraph())
	{
		CachedName = BaseTerminalGraph->GetDisplayName();

		OnFunctionMetaDataChangedPtr = MakeSharedVoid();
		GVoxelGraphTracker->OnTerminalGraphMetaDataChanged(*BaseTerminalGraph).Add(FOnVoxelGraphChanged::Make(OnFunctionMetaDataChangedPtr, this, [=, this]
		{
			RefreshNode();
		}));
	}

	Super::AllocateDefaultPins();
}

FText UVoxelGraphNode_CallMemberFunction::GetNodeTitle(const ENodeTitleType::Type TitleType) const
{
	if (!GetBaseTerminalGraph())
	{
		return FText::FromString(CachedName);
	}

	if (!bCallParent)
	{
		return Super::GetNodeTitle(TitleType);
	}

	return FText::FromString("Call Parent: " + Super::GetNodeTitle(TitleType).ToString());
}

FLinearColor UVoxelGraphNode_CallMemberFunction::GetNodeTitleColor() const
{
	if (bCallParent)
	{
		return GetDefault<UGraphEditorSettings>()->ParentFunctionCallNodeTitleColor;
	}

	return Super::GetNodeTitleColor();
}

